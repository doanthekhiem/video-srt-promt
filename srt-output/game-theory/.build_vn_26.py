print(f"Translations count: {len(TRANSLATIONS)}")

# Build VN
# Each entry's "marker" can be:
#   int  → last EN block idx (use that block's end time)
#   float → literal end time in seconds (used when splitting a block across multiple VN sentences)
output = []
prev_end_sec = en_by_idx[1][3]  # start of EN block 1
prev_end_idx = 0
errors = []
for i, (marker, vn_text) in enumerate(TRANSLATIONS, start=1):
    if isinstance(marker, float):
        end_sec = marker
        start_sec = prev_end_sec
        if end_sec <= start_sec:
            errors.append(f"Block {i}: float end {end_sec:.3f} <= prev {prev_end_sec:.3f}")
            continue
        start_ts = sec_to_ts(start_sec)
        end_ts = sec_to_ts(end_sec)
        output.append(f"{i}\n{start_ts} --> {end_ts}\n{vn_text}\n")
        prev_end_sec = end_sec
        # Don't change prev_end_idx — next int marker will keep aligned
    else:
        last_idx = marker
        first_idx = prev_end_idx + 1
        if last_idx < first_idx:
            errors.append(f"Block {i}: last_idx {last_idx} < first_idx {first_idx}")
            continue
        if last_idx not in en_by_idx:
            errors.append(f"Block {i}: last_idx {last_idx} not found")
            continue
        # Start: previous end (carry over from float if used), or EN block first_idx start
        if abs(prev_end_sec - en_by_idx[first_idx][3]) < 0.5 or prev_end_sec < en_by_idx[first_idx][3]:
            start_sec = en_by_idx[first_idx][3]
        else:
            start_sec = prev_end_sec  # we're mid-block from a float split
        end_sec = en_by_idx[last_idx][4]
        start_ts = sec_to_ts(start_sec)
        end_ts = sec_to_ts(end_sec)
        output.append(f"{i}\n{start_ts} --> {end_ts}\n{vn_text}\n")
        prev_end_sec = end_sec
        prev_end_idx = last_idx

if errors:
    print("ERRORS:")
    for e in errors[:20]:
        print(" ", e)

print(f"Wrote {len(output)} VN blocks. Last EN idx covered: {prev_end_idx}/{max_idx}")

