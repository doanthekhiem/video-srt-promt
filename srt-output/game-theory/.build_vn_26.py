def ts_to_sec(ts):
    m = re.match(r"(\d{2}):(\d{2}):(\d{2})[,.](\d{3})", ts.strip())
    if not m: return None
    h, mn, s, ms = map(int, m.groups())
    return h*3600 + mn*60 + s + ms/1000

def sec_to_ts(sec):
    if sec < 0: sec = 0
    h = int(sec // 3600); sec -= h*3600
    m = int(sec // 60);   sec -= m*60
    s = int(sec)
    ms = int(round((sec - s) * 1000))
    if ms == 1000: s += 1; ms = 0
    return f"{h:02d}:{m:02d}:{s:02d},{ms:03d}"

def parse_en(path):
    with open(path, "r", encoding="utf-8-sig") as f:
        content = f.read()
    blocks = []
    for raw in re.split(r"\n\s*\n", content.strip()):
        lines = [l for l in raw.strip().split("\n") if l.strip()]
        if len(lines) < 3:
            continue
        try:
            idx = int(lines[0].strip())
        except ValueError:
            continue
        ts = lines[1].strip()
        text = " ".join(lines[2:]).strip()
        try:
            start_sec = ts_to_sec(ts.split("-->")[0])
            end_sec = ts_to_sec(ts.split("-->")[1])
        except Exception:
            continue
        blocks.append((idx, ts, text, start_sec, end_sec))
    blocks.sort(key=lambda b: b[0])
    return blocks

